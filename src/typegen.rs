    #[test]
    fn render_sorted_output() {
        let mut registry = Registry::new();
        registry.register_collection(CollectionDefinition {
            slug: "zebras".to_string(),
            labels: CollectionLabels::default(),
            timestamps: true,
            fields: vec![],
            admin: CollectionAdmin::default(),
            hooks: CollectionHooks::default(),
            auth: None,
            upload: None,
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        });
        registry.register_collection(CollectionDefinition {
            slug: "apples".to_string(),
            labels: CollectionLabels::default(),
            timestamps: true,
            fields: vec![],
            admin: CollectionAdmin::default(),
            hooks: CollectionHooks::default(),
            auth: None,
            upload: None,
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        });

        let output = render(&registry);
        let apples_pos = output.find("crap.data.Apples").unwrap();
        let zebras_pos = output.find("crap.data.Zebras").unwrap();
        assert!(apples_pos < zebras_pos, "Collections should be sorted alphabetically");
    }

    #[test]
    fn render_find_overloads_output() {
        let mut registry = Registry::new();
        registry.register_collection(CollectionDefinition {
            slug: "posts".to_string(),
            labels: CollectionLabels::default(),
            timestamps: true,
            fields: vec![text_field("title", true)],
            admin: CollectionAdmin::default(),
            hooks: CollectionHooks::default(),
            auth: None,
            upload: None,
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        });
        registry.register_collection(CollectionDefinition {
            slug: "pages".to_string(),
            labels: CollectionLabels::default(),
            timestamps: true,
            fields: vec![text_field("body", false)],
            admin: CollectionAdmin::default(),
            hooks: CollectionHooks::default(),
            auth: None,
            upload: None,
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        });

        let output = render(&registry);

        // Overloads present for both collections (sorted)
        assert!(output.contains(
            "---@overload fun(collection: \"pages\", query?: crap.query.Pages): crap.find_result.Pages"
        ));
        assert!(output.contains(
            "---@overload fun(collection: \"posts\", query?: crap.query.Posts): crap.find_result.Posts"
        ));

        // pages overload comes before posts (alphabetical)
        let pages_pos = output.find("collection: \"pages\"").unwrap();
        let posts_pos = output.find("collection: \"posts\"").unwrap();
        assert!(pages_pos < posts_pos);

        // Generic fallback signature
        assert!(output.contains("---@param collection string"));
        assert!(output.contains("---@param query? crap.FindQuery"));
        assert!(output.contains("---@return crap.FindResult"));
        assert!(output.contains("function crap.collections.find(collection, query) end"));
    }

    #[test]
    fn render_find_overloads_empty_registry() {
        let registry = Registry::new();
        let output = render(&registry);
        // No overloads emitted for empty registry
        assert!(!output.contains("crap.collections.find"));
    }
