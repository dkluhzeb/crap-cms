//! Rust type definition generator — structs with serde derives for gRPC clients.

use std::fmt::Write;

use crate::core::{
    CollectionDefinition, FieldDefinition, FieldType, Registry, collection::GlobalDefinition,
};

use crate::typegen::{
    collect_sub_type_fields, is_optional, rel_has_many, sorted_collection_slugs,
    sorted_global_slugs, to_pascal_case,
};

/// Render all Rust type definitions.
pub(super) fn render(registry: &Registry) -> String {
    let mut out = String::new();
    writeln!(out, "// Auto-generated by crap-cms. Do not edit.").expect("write to String");
    writeln!(out).expect("write to String");
    writeln!(out, "use serde::{{Deserialize, Serialize}};").expect("write to String");
    writeln!(out).expect("write to String");
    render_rel_enum(&mut out);

    for slug in sorted_collection_slugs(registry) {
        let col = &registry.collections[slug];
        render_collection(&mut out, col);
    }

    for slug in sorted_global_slugs(registry) {
        let global = &registry.globals[slug];
        render_global(&mut out, global);
    }

    out
}

/// Render the `Rel<T>` enum and convenience methods.
fn render_rel_enum(out: &mut String) {
    writeln!(
        out,
        "/// A relationship field: either an ID string (depth=0)"
    )
    .expect("write");
    writeln!(out, "/// or a populated document (depth>=1).").expect("write");
    writeln!(out, "#[derive(Debug, Clone, Serialize, Deserialize)]").expect("write");
    writeln!(out, "#[serde(untagged)]").expect("write");
    writeln!(out, "pub enum Rel<T> {{").expect("write");
    writeln!(out, "    Doc(Box<T>),").expect("write");
    writeln!(out, "    Id(String),").expect("write");
    writeln!(out, "}}").expect("write");
    writeln!(out).expect("write");
    writeln!(out, "impl<T> Rel<T> {{").expect("write");
    writeln!(out, "    /// Get the ID if unpopulated.").expect("write");
    writeln!(out, "    pub fn as_id(&self) -> Option<&str> {{").expect("write");
    writeln!(
        out,
        "        match self {{ Rel::Id(id) => Some(id), _ => None }}"
    )
    .expect("write");
    writeln!(out, "    }}").expect("write");
    writeln!(out).expect("write");
    writeln!(out, "    /// Get the populated document.").expect("write");
    writeln!(out, "    pub fn as_doc(&self) -> Option<&T> {{").expect("write");
    writeln!(
        out,
        "        match self {{ Rel::Doc(d) => Some(d), _ => None }}"
    )
    .expect("write");
    writeln!(out, "    }}").expect("write");
    writeln!(out, "}}").expect("write");
    writeln!(out).expect("write");
}

/// Render type definitions for a single collection.
fn render_collection(out: &mut String, col: &CollectionDefinition) {
    let pascal = to_pascal_case(&col.slug);

    // Sub-types (Array rows and Group shapes)
    for stf in collect_sub_type_fields(&col.fields) {
        let sub_pascal = format!("{}{}", pascal, to_pascal_case(&stf.field.name));
        writeln!(out, "#[derive(Debug, Clone, Serialize, Deserialize)]").expect("write to String");
        writeln!(out, "pub struct {} {{", sub_pascal).expect("write to String");
        for sf in &stf.field.fields {
            write_field_with_context(out, sf, &pascal);
        }
        writeln!(out, "}}\n").expect("write to String");
    }

    // Document struct
    writeln!(out, "#[derive(Debug, Clone, Serialize, Deserialize)]").expect("write to String");
    writeln!(out, "pub struct {} {{", pascal).expect("write to String");
    writeln!(out, "    pub id: String,").expect("write to String");
    for f in &col.fields {
        write_field_with_context(out, f, &pascal);
    }
    if col.timestamps {
        writeln!(
            out,
            "    #[serde(skip_serializing_if = \"Option::is_none\")]"
        )
        .expect("write to String");
        writeln!(out, "    pub created_at: Option<String>,").expect("write to String");
        writeln!(
            out,
            "    #[serde(skip_serializing_if = \"Option::is_none\")]"
        )
        .expect("write to String");
        writeln!(out, "    pub updated_at: Option<String>,").expect("write to String");
    }
    writeln!(out, "}}\n").expect("write to String");
}

/// Render type definitions for a single global.
fn render_global(out: &mut String, global: &GlobalDefinition) {
    let pascal = to_pascal_case(&global.slug);

    // Sub-types (Array rows and Group shapes)
    for stf in collect_sub_type_fields(&global.fields) {
        let sub_pascal = format!("{}{}", pascal, to_pascal_case(&stf.field.name));
        writeln!(out, "#[derive(Debug, Clone, Serialize, Deserialize)]").expect("write to String");
        writeln!(out, "pub struct {} {{", sub_pascal).expect("write to String");
        for sf in &stf.field.fields {
            write_field_with_context(out, sf, &pascal);
        }
        writeln!(out, "}}\n").expect("write to String");
    }

    writeln!(out, "#[derive(Debug, Clone, Serialize, Deserialize)]").expect("write to String");
    writeln!(out, "pub struct {} {{", pascal).expect("write to String");
    writeln!(out, "    pub id: String,").expect("write to String");
    for f in &global.fields {
        write_field_with_context(out, f, &pascal);
    }
    writeln!(
        out,
        "    #[serde(skip_serializing_if = \"Option::is_none\")]"
    )
    .expect("write to String");
    writeln!(out, "    pub created_at: Option<String>,").expect("write to String");
    writeln!(
        out,
        "    #[serde(skip_serializing_if = \"Option::is_none\")]"
    )
    .expect("write to String");
    writeln!(out, "    pub updated_at: Option<String>,").expect("write to String");
    writeln!(out, "}}\n").expect("write to String");
}

/// Write a single field's type definition.
fn write_field_with_context(out: &mut String, field: &FieldDefinition, parent_pascal: &str) {
    // Row is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Row {
        for sub in &field.fields {
            write_field_with_context(out, sub, parent_pascal);
        }
        return;
    }
    // Collapsible is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Collapsible {
        for sub in &field.fields {
            write_field_with_context(out, sub, parent_pascal);
        }
        return;
    }
    // Tabs is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Tabs {
        for tab in &field.tabs {
            for sub in &tab.fields {
                write_field_with_context(out, sub, parent_pascal);
            }
        }
        return;
    }
    // Emit a comment for polymorphic relationships listing target collections
    if field.field_type == FieldType::Relationship
        && let Some(rc) = &field.relationship
        && rc.is_polymorphic()
    {
        let targets = rc.all_collections().join(", ");
        writeln!(
            out,
            "    /// Polymorphic relationship — targets: {}",
            targets
        )
        .expect("write to String");
    }
    let rust_type = field_to_rust(field, parent_pascal);

    if is_optional(field) {
        writeln!(
            out,
            "    #[serde(skip_serializing_if = \"Option::is_none\")]"
        )
        .expect("write to String");
        writeln!(out, "    pub {}: Option<{}>,", field.name, rust_type).expect("write to String");
    } else {
        writeln!(out, "    pub {}: {},", field.name, rust_type).expect("write to String");
    }
}

/// Map a field definition to its Rust type string.
fn field_to_rust(field: &FieldDefinition, parent_pascal: &str) -> String {
    match &field.field_type {
        FieldType::Text => {
            if field.has_many {
                "Vec<String>".to_string()
            } else {
                "String".to_string()
            }
        }
        FieldType::Textarea
        | FieldType::Email
        | FieldType::Date
        | FieldType::Richtext
        | FieldType::Code => "String".to_string(),
        FieldType::Upload => {
            let target = field
                .relationship
                .as_ref()
                .filter(|rc| !rc.collection.is_empty())
                .map(|rc| to_pascal_case(&rc.collection));

            match target {
                Some(t) if rel_has_many(field) => format!("Vec<Rel<{}>>", t),
                Some(t) => format!("Rel<{}>", t),
                None if rel_has_many(field) => "Vec<String>".to_string(),
                None => "String".to_string(),
            }
        }
        FieldType::Select | FieldType::Radio => {
            if field.has_many {
                "Vec<String>".to_string()
            } else {
                "String".to_string()
            }
        }
        FieldType::Number => {
            if field.has_many {
                "Vec<f64>".to_string()
            } else {
                "f64".to_string()
            }
        }
        FieldType::Checkbox => "bool".to_string(),
        FieldType::Json => "serde_json::Value".to_string(),
        FieldType::Relationship => match &field.relationship {
            Some(rc) if rc.is_polymorphic() && rc.has_many => "Vec<String>".to_string(),
            Some(rc) if rc.is_polymorphic() => "String".to_string(),
            Some(rc) if rc.has_many => format!("Vec<Rel<{}>>", to_pascal_case(&rc.collection)),
            Some(rc) => format!("Rel<{}>", to_pascal_case(&rc.collection)),
            None => "String".to_string(),
        },
        FieldType::Array => {
            if field.fields.is_empty() {
                "Vec<serde_json::Value>".to_string()
            } else {
                format!("Vec<{}{}>", parent_pascal, to_pascal_case(&field.name))
            }
        }
        FieldType::Group => {
            if field.fields.is_empty() {
                "serde_json::Value".to_string()
            } else {
                format!("{}{}", parent_pascal, to_pascal_case(&field.name))
            }
        }
        FieldType::Row => "serde_json::Value".to_string(), // layout-only; sub-fields are promoted
        FieldType::Collapsible => "serde_json::Value".to_string(), // layout-only; sub-fields are promoted
        FieldType::Tabs => "serde_json::Value".to_string(), // layout-only; sub-fields are promoted
        FieldType::Blocks => "Vec<serde_json::Value>".to_string(),
        FieldType::Join => "Vec<serde_json::Value>".to_string(),
    }
}

