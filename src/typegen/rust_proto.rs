//! Rust proto conversion generator — `from_document` impls using prost_types.
//!
//! Generates `generated_proto.rs` with direct prost_types extraction:
//! no JSON intermediate, no serde deserialization in the hot path.

use std::fmt::Write;

use crate::core::{
    CollectionDefinition, FieldDefinition, FieldType, Registry, collection::GlobalDefinition,
};

use crate::typegen::{
    collect_sub_type_fields, is_optional, rel_has_many, sorted_collection_slugs,
    sorted_global_slugs, to_pascal_case,
};

/// Render proto conversion code for all collections and globals.
pub(super) fn render(registry: &Registry, proto_mod: &str) -> String {
    let mut out = String::new();

    w!(out, "// Auto-generated by crap-cms. Do not edit.");
    w!(out, "//");
    w!(out, "// Proto-to-struct conversion for prost_types.");
    w!(
        out,
        "// Import the generated structs from generated.rs alongside this file."
    );
    w!(out, "");
    w!(out, "use prost_types::value::Kind;");
    w!(out, "use {}::Document;", proto_mod);
    w!(out, "");
    w!(out, "use super::generated::*;");
    w!(out, "");

    render_helpers(&mut out);

    for slug in sorted_collection_slugs(registry) {
        let col = &registry.collections[slug];
        render_collection_impl(&mut out, col);
    }

    for slug in sorted_global_slugs(registry) {
        let global = &registry.globals[slug];
        render_global_impl(&mut out, global);
    }

    out
}

/// Helper macro to reduce writeln boilerplate.
macro_rules! w {
    ($out:expr, $($arg:tt)*) => {
        writeln!($out, $($arg)*).expect("write to String")
    };
}
use w;

/// Render shared helper functions for field extraction.
fn render_helpers(out: &mut String) {
    w!(
        out,
        "// ── Extraction helpers ─────────────────────────────────────"
    );
    w!(out, "");
    w!(out, "fn get_str(doc: &Document, name: &str) -> String {{");
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(
        out,
        "            Some(Kind::StringValue(s)) => Some(s.clone()),"
    );
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "        .unwrap_or_default()");
    w!(out, "}}");
    w!(out, "");
    w!(
        out,
        "fn get_str_opt(doc: &Document, name: &str) -> Option<String> {{"
    );
    w!(out, "    let s = get_str(doc, name);");
    w!(out, "    if s.is_empty() {{ None }} else {{ Some(s) }}");
    w!(out, "}}");
    w!(out, "");
    w!(out, "fn get_num(doc: &Document, name: &str) -> f64 {{");
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(out, "            Some(Kind::NumberValue(n)) => Some(*n),");
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "        .unwrap_or(0.0)");
    w!(out, "}}");
    w!(out, "");
    w!(
        out,
        "fn get_num_opt(doc: &Document, name: &str) -> Option<f64> {{"
    );
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(out, "            Some(Kind::NumberValue(n)) => Some(*n),");
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "}}");
    w!(out, "");
    w!(out, "fn get_bool(doc: &Document, name: &str) -> bool {{");
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(out, "            Some(Kind::BoolValue(b)) => Some(*b),");
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "        .unwrap_or(false)");
    w!(out, "}}");
    w!(out, "");
    w!(
        out,
        "fn get_bool_opt(doc: &Document, name: &str) -> Option<bool> {{"
    );
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(out, "            Some(Kind::BoolValue(b)) => Some(*b),");
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "}}");
    w!(out, "");
    w!(
        out,
        "fn get_str_list(doc: &Document, name: &str) -> Vec<String> {{"
    );
    w!(out, "    doc.fields.as_ref()");
    w!(out, "        .and_then(|f| f.fields.get(name))");
    w!(out, "        .and_then(|v| match &v.kind {{");
    w!(out, "            Some(Kind::ListValue(list)) => Some(");
    w!(out, "                list.values.iter()");
    w!(out, "                    .filter_map(|v| match &v.kind {{");
    w!(
        out,
        "                        Some(Kind::StringValue(s)) => Some(s.clone()),"
    );
    w!(out, "                        _ => None,");
    w!(out, "                    }})");
    w!(out, "                    .collect()");
    w!(out, "            ),");
    w!(out, "            _ => None,");
    w!(out, "        }})");
    w!(out, "        .unwrap_or_default()");
    w!(out, "}}");
    w!(out, "");
}

/// Render `from_document` impl for a collection.
fn render_collection_impl(out: &mut String, col: &CollectionDefinition) {
    let pascal = to_pascal_case(&col.slug);

    // Sub-type from_struct impls for arrays
    for stf in collect_sub_type_fields(&col.fields) {
        let sub_pascal = format!("{}{}", pascal, to_pascal_case(&stf.field.name));
        render_sub_type_from_struct(out, &sub_pascal, &stf.field.fields);
    }

    w!(out, "impl {} {{", pascal);
    w!(out, "    pub fn from_document(doc: &Document) -> Self {{");
    w!(out, "        Self {{");
    w!(out, "            id: doc.id.clone(),");

    render_field_extractions(out, &col.fields, &pascal, "doc");

    if col.timestamps {
        w!(out, "            created_at: doc.created_at.clone(),");
        w!(out, "            updated_at: doc.updated_at.clone(),");
    }

    w!(out, "        }}");
    w!(out, "    }}");
    w!(out, "}}");
    w!(out, "");
}

/// Render `from_document` impl for a global.
fn render_global_impl(out: &mut String, global: &GlobalDefinition) {
    let pascal = to_pascal_case(&global.slug);

    for stf in collect_sub_type_fields(&global.fields) {
        let sub_pascal = format!("{}{}", pascal, to_pascal_case(&stf.field.name));
        render_sub_type_from_struct(out, &sub_pascal, &stf.field.fields);
    }

    w!(out, "impl {} {{", pascal);
    w!(out, "    pub fn from_document(doc: &Document) -> Self {{");
    w!(out, "        Self {{");
    w!(out, "            id: doc.id.clone(),");

    render_field_extractions(out, &global.fields, &pascal, "doc");

    w!(out, "            created_at: doc.created_at.clone(),");
    w!(out, "            updated_at: doc.updated_at.clone(),");
    w!(out, "        }}");
    w!(out, "    }}");
    w!(out, "}}");
    w!(out, "");
}

/// Render field extraction lines for a struct's from_document body.
fn render_field_extractions(
    out: &mut String,
    fields: &[FieldDefinition],
    parent_pascal: &str,
    doc_var: &str,
) {
    for f in fields {
        // Layout-only wrappers — promote sub-fields
        if f.field_type == FieldType::Row || f.field_type == FieldType::Collapsible {
            render_field_extractions(out, &f.fields, parent_pascal, doc_var);
            continue;
        }

        if f.field_type == FieldType::Tabs {
            for tab in &f.tabs {
                render_field_extractions(out, &tab.fields, parent_pascal, doc_var);
            }
            continue;
        }

        let extraction = field_extraction(f, parent_pascal, doc_var);
        w!(out, "            {}: {},", f.name, extraction);
    }
}

/// Generate the extraction expression for a single field.
fn field_extraction(field: &FieldDefinition, parent_pascal: &str, doc_var: &str) -> String {
    let name = &field.name;
    let optional = is_optional(field);

    match &field.field_type {
        FieldType::Text
        | FieldType::Textarea
        | FieldType::Email
        | FieldType::Date
        | FieldType::Richtext
        | FieldType::Code
        | FieldType::Select
        | FieldType::Radio => {
            if field.has_many {
                if optional {
                    format!(
                        "{{ let v = get_str_list({doc_var}, \"{name}\"); if v.is_empty() {{ None }} else {{ Some(v) }} }}"
                    )
                } else {
                    format!("get_str_list({doc_var}, \"{name}\")")
                }
            } else if optional {
                format!("get_str_opt({doc_var}, \"{name}\")")
            } else {
                format!("get_str({doc_var}, \"{name}\")")
            }
        }

        FieldType::Number => {
            if field.has_many {
                // Number lists — extract from ListValue
                if optional {
                    format!("None /* TODO: number list {name} */")
                } else {
                    format!("vec![] /* TODO: number list {name} */")
                }
            } else if optional {
                format!("get_num_opt({doc_var}, \"{name}\")")
            } else {
                format!("get_num({doc_var}, \"{name}\")")
            }
        }

        FieldType::Checkbox => {
            if optional {
                format!("get_bool_opt({doc_var}, \"{name}\")")
            } else {
                format!("get_bool({doc_var}, \"{name}\")")
            }
        }

        FieldType::Relationship | FieldType::Upload => {
            let has_many = rel_has_many(field);
            if has_many {
                if optional {
                    format!(
                        "{{ let v = get_str_list({doc_var}, \"{name}\"); if v.is_empty() {{ None }} else {{ Some(v) }} }}"
                    )
                } else {
                    format!("get_str_list({doc_var}, \"{name}\")")
                }
            } else if optional {
                format!("get_str_opt({doc_var}, \"{name}\")")
            } else {
                format!("get_str({doc_var}, \"{name}\")")
            }
        }

        FieldType::Array => {
            if field.fields.is_empty() {
                if optional {
                    "None /* untyped array */".to_string()
                } else {
                    "vec![] /* untyped array */".to_string()
                }
            } else {
                let sub_pascal = format!("{}{}", parent_pascal, to_pascal_case(name));
                let extract = format!(
                    "{doc_var}.fields.as_ref()\
                    .and_then(|f| f.fields.get(\"{name}\"))\
                    .and_then(|v| match &v.kind {{\
                        Some(Kind::ListValue(list)) => Some(\
                            list.values.iter()\
                                .filter_map(|v| match &v.kind {{\
                                    Some(Kind::StructValue(s)) => Some({sub_pascal}::from_struct(s)),\
                                    _ => None,\
                                }})\
                                .collect::<Vec<_>>()\
                        ),\
                        _ => None,\
                    }})"
                );
                if optional {
                    extract
                } else {
                    format!("{extract}.unwrap_or_default()")
                }
            }
        }

        FieldType::Json | FieldType::Blocks | FieldType::Join | FieldType::Group => {
            // These map to serde_json::Value — skip proto extraction for now
            if optional {
                "None /* complex field */".to_string()
            } else {
                "Default::default() /* complex field */".to_string()
            }
        }

        // Layout wrappers handled in render_field_extractions
        FieldType::Row | FieldType::Collapsible | FieldType::Tabs => {
            unreachable!("layout wrappers handled above")
        }
    }
}

/// Render a `from_struct` impl for an array sub-type.
fn render_sub_type_from_struct(out: &mut String, pascal: &str, fields: &[FieldDefinition]) {
    w!(out, "impl {} {{", pascal);
    w!(
        out,
        "    fn from_struct(s: &prost_types::Struct) -> Self {{"
    );
    w!(out, "        Self {{");

    for f in fields {
        let name = &f.name;
        let extraction = sub_field_extraction(f);
        w!(out, "            {}: {},", name, extraction);
    }

    w!(out, "        }}");
    w!(out, "    }}");
    w!(out, "}}");
    w!(out, "");
}

/// Generate extraction for a sub-type field (from prost_types::Struct, not Document).
fn sub_field_extraction(field: &FieldDefinition) -> String {
    let name = &field.name;
    let optional = is_optional(field);

    match &field.field_type {
        FieldType::Text
        | FieldType::Textarea
        | FieldType::Email
        | FieldType::Date
        | FieldType::Richtext
        | FieldType::Code
        | FieldType::Select
        | FieldType::Radio => {
            let base = format!(
                "s.fields.get(\"{name}\").and_then(|v| match &v.kind {{ Some(Kind::StringValue(s)) => Some(s.clone()), _ => None }})"
            );
            if optional {
                base
            } else {
                format!("{base}.unwrap_or_default()")
            }
        }

        FieldType::Number => {
            let base = format!(
                "s.fields.get(\"{name}\").and_then(|v| match &v.kind {{ Some(Kind::NumberValue(n)) => Some(*n), _ => None }})"
            );
            if optional {
                base
            } else {
                format!("{base}.unwrap_or(0.0)")
            }
        }

        FieldType::Checkbox => {
            let base = format!(
                "s.fields.get(\"{name}\").and_then(|v| match &v.kind {{ Some(Kind::BoolValue(b)) => Some(*b), _ => None }})"
            );
            if optional {
                base
            } else {
                format!("{base}.unwrap_or(false)")
            }
        }

        _ => {
            if optional {
                "None".to_string()
            } else {
                "Default::default()".to_string()
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::core::field::RelationshipConfig;

    fn text_field(name: &str, required: bool) -> FieldDefinition {
        FieldDefinition::builder(name, FieldType::Text)
            .required(required)
            .build()
    }

    fn make_col(slug: &str, fields: Vec<FieldDefinition>) -> CollectionDefinition {
        let mut def = CollectionDefinition::new(slug);
        def.timestamps = true;
        def.fields = fields;
        def
    }

    #[test]
    fn proto_collection_output() {
        let col = make_col(
            "posts",
            vec![text_field("title", true), text_field("content", false)],
        );
        let mut out = String::new();
        render_collection_impl(&mut out, &col);

        assert!(out.contains("impl Posts {"));
        assert!(out.contains("fn from_document(doc: &Document) -> Self"));
        assert!(out.contains("id: doc.id.clone()"));
        assert!(out.contains("title: get_str(doc, \"title\")"));
        assert!(out.contains("content: get_str_opt(doc, \"content\")"));
        assert!(out.contains("created_at: doc.created_at.clone()"));
    }

    #[test]
    fn proto_number_and_bool_fields() {
        let col = make_col(
            "items",
            vec![
                FieldDefinition::builder("price", FieldType::Number)
                    .required(true)
                    .build(),
                FieldDefinition::builder("active", FieldType::Checkbox).build(),
            ],
        );
        let mut out = String::new();
        render_collection_impl(&mut out, &col);

        assert!(out.contains("price: get_num(doc, \"price\")"));
        assert!(out.contains("active: get_bool_opt(doc, \"active\")"));
    }

    #[test]
    fn proto_relationship_fields() {
        let col = make_col(
            "posts",
            vec![
                FieldDefinition::builder("author", FieldType::Relationship)
                    .required(true)
                    .relationship(RelationshipConfig::new("users", false))
                    .build(),
                FieldDefinition::builder("tags", FieldType::Relationship)
                    .relationship(RelationshipConfig::new("tags", true))
                    .build(),
            ],
        );
        let mut out = String::new();
        render_collection_impl(&mut out, &col);

        assert!(
            out.contains("author: get_str(doc, \"author\")"),
            "required has-one: {}",
            out
        );
        assert!(
            out.contains("get_str_list(doc, \"tags\")"),
            "optional has-many: {}",
            out
        );
    }

    #[test]
    fn proto_array_with_subfields() {
        let col = make_col(
            "posts",
            vec![
                FieldDefinition::builder("items", FieldType::Array)
                    .fields(vec![
                        text_field("label", true),
                        FieldDefinition::builder("done", FieldType::Checkbox).build(),
                    ])
                    .build(),
            ],
        );
        let mut out = String::new();
        render_collection_impl(&mut out, &col);

        assert!(
            out.contains("impl PostsItems {"),
            "should generate sub-type impl: {}",
            out
        );
        assert!(
            out.contains("fn from_struct(s: &prost_types::Struct)"),
            "should have from_struct: {}",
            out
        );
        assert!(
            out.contains("PostsItems::from_struct(s)"),
            "should call from_struct in array extraction: {}",
            out
        );
    }

    #[test]
    fn proto_helpers_output() {
        let mut out = String::new();
        render_helpers(&mut out);

        assert!(out.contains("fn get_str(doc: &Document, name: &str) -> String"));
        assert!(out.contains("fn get_str_opt(doc: &Document, name: &str) -> Option<String>"));
        assert!(out.contains("fn get_num(doc: &Document, name: &str) -> f64"));
        assert!(out.contains("fn get_bool(doc: &Document, name: &str) -> bool"));
        assert!(out.contains("fn get_str_list(doc: &Document, name: &str) -> Vec<String>"));
    }

    #[test]
    fn proto_full_render() {
        let mut registry = Registry::new();
        registry.register_collection(make_col("posts", vec![text_field("title", true)]));
        let out = render(&registry, "crate::proto");

        assert!(out.contains("use crate::proto::Document;"));
        assert!(out.contains("use prost_types::value::Kind;"));
        assert!(out.contains("impl Posts {"));
    }

    #[test]
    fn proto_row_promotes_subfields() {
        let col = make_col(
            "items",
            vec![
                FieldDefinition::builder("row", FieldType::Row)
                    .fields(vec![text_field("first", true), text_field("last", false)])
                    .build(),
            ],
        );
        let mut out = String::new();
        render_collection_impl(&mut out, &col);

        assert!(
            out.contains("first: get_str(doc, \"first\")"),
            "row sub-fields promoted: {}",
            out
        );
        assert!(
            out.contains("last: get_str_opt(doc, \"last\")"),
            "row sub-fields promoted: {}",
            out
        );
        assert!(
            !out.contains("row:"),
            "row field itself should not appear: {}",
            out
        );
    }
}
