//! Rust proto conversion generator — `from_document` impls using prost_types.
//!
//! Generates `generated_proto.rs` with direct prost_types extraction:
//! no JSON intermediate, no serde deserialization in the hot path.

use std::fmt::Write;

use crate::core::{
    CollectionDefinition, FieldDefinition, FieldType, Registry, collection::GlobalDefinition,
};

use crate::typegen::{
    collect_sub_type_fields, is_optional, rel_has_many, sorted_collection_slugs,
    sorted_global_slugs, to_pascal_case,
};

/// Render proto conversion code for all collections and globals.
pub(super) fn render(registry: &Registry, proto_mod: &str) -> String {
    let mut out = String::new();

    w!(out, "// Auto-generated by crap-cms. Do not edit.");
    w!(out, "//");
    w!(out, "// Proto-to-struct conversion for prost_types.");
    w!(
        out,
        "// Import the generated structs from generated.rs alongside this file."
    );
    w!(out, "");
    w!(out, "use prost_types::value::Kind;");
    w!(out, "use {}::Document;", proto_mod);
    w!(out, "");
    w!(out, "use super::generated::*;");
    w!(out, "");

    render_from_document_trait(&mut out);
    render_helpers(&mut out);
    render_rel_helpers(&mut out);

    for slug in sorted_collection_slugs(registry) {
        let col = &registry.collections[slug];
        render_collection_impl(&mut out, col);
    }

    for slug in sorted_global_slugs(registry) {
        let global = &registry.globals[slug];
        render_global_impl(&mut out, global);
    }

    out
}

/// Helper macro to reduce writeln boilerplate.
macro_rules! w {
    ($out:expr, $($arg:tt)*) => {
        writeln!($out, $($arg)*).expect("write to String")
    };
}
use w;

