//! TypeScript type definition generator — interfaces for gRPC client wrappers.

use std::fmt::Write;

use crate::core::Registry;
use crate::core::collection::{CollectionDefinition, GlobalDefinition};
use crate::core::field::{FieldDefinition, FieldType};

use super::{
    is_optional, rel_has_many, sorted_collection_slugs, sorted_global_slugs, to_pascal_case,
};

pub(super) fn render(registry: &Registry) -> String {
    let mut out = String::from("// Auto-generated by crap-cms. Do not edit.\n\n");

    for slug in sorted_collection_slugs(registry) {
        let col = &registry.collections[slug];
        render_collection(&mut out, col);
    }

    for slug in sorted_global_slugs(registry) {
        let global = &registry.globals[slug];
        render_global(&mut out, global);
    }

    render_collection_slug_type(&mut out, registry);

    out
}

fn render_collection(out: &mut String, col: &CollectionDefinition) {
    let pascal = to_pascal_case(&col.slug);

    // Array sub-types
    for f in &col.fields {
        if f.field_type == FieldType::Array && !f.fields.is_empty() {
            let sub_pascal = format!("{}{}", pascal, to_pascal_case(&f.name));
            writeln!(out, "export interface {} {{", sub_pascal).expect("write to String");
            for sf in &f.fields {
                write_field(out, sf);
            }
            writeln!(out, "}}\n").expect("write to String");
        }
    }

    // Input data interface
    writeln!(out, "/** Input data for creating/updating a {} */", pascal).expect("write to String");
    writeln!(out, "export interface {}Data {{", pascal).expect("write to String");
    for f in &col.fields {
        write_field_with_context(out, f, &pascal);
    }
    writeln!(out, "}}\n").expect("write to String");

    // Document interface (extends data with id + timestamps)
    writeln!(out, "/** {} document returned from the API */", pascal).expect("write to String");
    writeln!(
        out,
        "export interface {}Document extends {}Data {{",
        pascal, pascal
    )
    .expect("write to String");
    writeln!(out, "  id: string;").expect("write to String");
    if col.timestamps {
        writeln!(out, "  created_at?: string;").expect("write to String");
        writeln!(out, "  updated_at?: string;").expect("write to String");
    }
    writeln!(out, "}}\n").expect("write to String");
}

fn render_global(out: &mut String, global: &GlobalDefinition) {
    let pascal = to_pascal_case(&global.slug);

    writeln!(out, "/** Input data for {} global */", pascal).expect("write to String");
    writeln!(out, "export interface {}Data {{", pascal).expect("write to String");
    for f in &global.fields {
        write_field(out, f);
    }
    writeln!(out, "}}\n").expect("write to String");

    writeln!(out, "/** {} global document */", pascal).expect("write to String");
    writeln!(
        out,
        "export interface {}Document extends {}Data {{",
        pascal, pascal
    )
    .expect("write to String");
    writeln!(out, "  id: string;").expect("write to String");
    writeln!(out, "  created_at?: string;").expect("write to String");
    writeln!(out, "  updated_at?: string;").expect("write to String");
    writeln!(out, "}}\n").expect("write to String");
}

fn render_collection_slug_type(out: &mut String, registry: &Registry) {
    let slugs = sorted_collection_slugs(registry);
    if slugs.is_empty() {
        return;
    }
    let union = slugs
        .iter()
        .map(|s| format!("\"{}\"", s))
        .collect::<Vec<_>>()
        .join(" | ");
    writeln!(out, "/** All collection slugs */").expect("write to String");
    writeln!(out, "export type CollectionSlug = {};", union).expect("write to String");
}

fn write_polymorphic_comment(out: &mut String, field: &FieldDefinition, indent: &str) {
    if field.field_type == FieldType::Relationship {
        if let Some(rc) = &field.relationship {
            if rc.is_polymorphic() {
                let targets = rc.all_collections().join(", ");
                writeln!(
                    out,
                    "{}/** Polymorphic relationship — targets: {} */",
                    indent, targets
                )
                .expect("write to String");
            }
        }
    }
}

fn write_field(out: &mut String, field: &FieldDefinition) {
    // Row is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Row {
        for sub in &field.fields {
            write_field(out, sub);
        }
        return;
    }
    // Collapsible is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Collapsible {
        for sub in &field.fields {
            write_field(out, sub);
        }
        return;
    }
    // Tabs is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Tabs {
        for tab in &field.tabs {
            for sub in &tab.fields {
                write_field(out, sub);
            }
        }
        return;
    }
    write_polymorphic_comment(out, field, "  ");
    let ts_type = field_to_ts(field, "");
    let opt = if is_optional(field) { "?" } else { "" };
    writeln!(out, "  {}{}: {};", field.name, opt, ts_type).expect("write to String");
}

fn write_field_with_context(out: &mut String, field: &FieldDefinition, parent_pascal: &str) {
    // Row is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Row {
        for sub in &field.fields {
            write_field_with_context(out, sub, parent_pascal);
        }
        return;
    }
    // Collapsible is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Collapsible {
        for sub in &field.fields {
            write_field_with_context(out, sub, parent_pascal);
        }
        return;
    }
    // Tabs is layout-only — promote sub-fields to parent level (no prefix)
    if field.field_type == FieldType::Tabs {
        for tab in &field.tabs {
            for sub in &tab.fields {
                write_field_with_context(out, sub, parent_pascal);
            }
        }
        return;
    }
    write_polymorphic_comment(out, field, "  ");
    let ts_type = field_to_ts(field, parent_pascal);
    let opt = if is_optional(field) { "?" } else { "" };
    writeln!(out, "  {}{}: {};", field.name, opt, ts_type).expect("write to String");
}

fn field_to_ts(field: &FieldDefinition, parent_pascal: &str) -> String {
    match &field.field_type {
        FieldType::Text => {
            if field.has_many {
                "string[]".to_string()
            } else {
                "string".to_string()
            }
        }
        FieldType::Textarea
        | FieldType::Email
        | FieldType::Date
        | FieldType::Richtext
        | FieldType::Code => "string".to_string(),
        FieldType::Number => {
            if field.has_many {
                "number[]".to_string()
            } else {
                "number".to_string()
            }
        }
        FieldType::Checkbox => "boolean".to_string(),
        FieldType::Json => "unknown".to_string(),
        FieldType::Select | FieldType::Radio => {
            let base = if field.options.is_empty() {
                "string".to_string()
            } else {
                field
                    .options
                    .iter()
                    .map(|o| format!("\"{}\"", o.value))
                    .collect::<Vec<_>>()
                    .join(" | ")
            };
            if field.has_many {
                if field.options.is_empty() {
                    "string[]".to_string()
                } else {
                    format!("({})[]", base)
                }
            } else {
                base
            }
        }
        FieldType::Relationship => match &field.relationship {
            Some(rc) if rc.is_polymorphic() && rc.has_many => "string[]".to_string(),
            Some(rc) if rc.is_polymorphic() => "string".to_string(),
            Some(rc) if rc.has_many => "string[]".to_string(),
            _ => "string".to_string(),
        },
        FieldType::Array => {
            if field.fields.is_empty() {
                "Record<string, unknown>[]".to_string()
            } else {
                format!("{}{}[]", parent_pascal, to_pascal_case(&field.name))
            }
        }
        FieldType::Group => "Record<string, unknown>".to_string(),
        FieldType::Row => "Record<string, unknown>".to_string(), // layout-only; sub-fields are promoted
        FieldType::Collapsible => "Record<string, unknown>".to_string(), // layout-only; sub-fields are promoted
        FieldType::Tabs => "Record<string, unknown>".to_string(), // layout-only; sub-fields are promoted
        FieldType::Upload => {
            if rel_has_many(field) {
                "string[]".to_string()
            } else {
                "string".to_string()
            }
        }
        FieldType::Blocks => "Record<string, unknown>[]".to_string(),
        FieldType::Join => "Record<string, unknown>[]".to_string(),
    }
}

