//! TypeScript type definition generator — interfaces for gRPC client wrappers.

use std::fmt::Write;

use crate::core::collection::{CollectionDefinition, GlobalDefinition};
use crate::core::field::{FieldDefinition, FieldType};
use crate::core::Registry;

use super::{is_optional, sorted_collection_slugs, sorted_global_slugs, to_pascal_case};

pub(super) fn render(registry: &Registry) -> String {
    let mut out = String::from("// Auto-generated by crap-cms. Do not edit.\n\n");

    for slug in sorted_collection_slugs(registry) {
        let col = &registry.collections[slug];
        render_collection(&mut out, col);
    }

    for slug in sorted_global_slugs(registry) {
        let global = &registry.globals[slug];
        render_global(&mut out, global);
    }

    render_collection_slug_type(&mut out, registry);

    out
}

fn render_collection(out: &mut String, col: &CollectionDefinition) {
    let pascal = to_pascal_case(&col.slug);

    // Array sub-types
    for f in &col.fields {
        if f.field_type == FieldType::Array && !f.fields.is_empty() {
            let sub_pascal = format!("{}{}", pascal, to_pascal_case(&f.name));
            writeln!(out, "export interface {} {{", sub_pascal).unwrap();
            for sf in &f.fields {
                write_field(out, sf);
            }
            writeln!(out, "}}\n").unwrap();
        }
    }

    // Input data interface
    writeln!(out, "/** Input data for creating/updating a {} */", pascal).unwrap();
    writeln!(out, "export interface {}Data {{", pascal).unwrap();
    for f in &col.fields {
        write_field_with_context(out, f, &pascal);
    }
    writeln!(out, "}}\n").unwrap();

    // Document interface (extends data with id + timestamps)
    writeln!(out, "/** {} document returned from the API */", pascal).unwrap();
    writeln!(out, "export interface {}Document extends {}Data {{", pascal, pascal).unwrap();
    writeln!(out, "  id: string;").unwrap();
    if col.timestamps {
        writeln!(out, "  created_at?: string;").unwrap();
        writeln!(out, "  updated_at?: string;").unwrap();
    }
    writeln!(out, "}}\n").unwrap();
}

fn render_global(out: &mut String, global: &GlobalDefinition) {
    let pascal = to_pascal_case(&global.slug);

    writeln!(out, "/** Input data for {} global */", pascal).unwrap();
    writeln!(out, "export interface {}Data {{", pascal).unwrap();
    for f in &global.fields {
        write_field(out, f);
    }
    writeln!(out, "}}\n").unwrap();

    writeln!(out, "/** {} global document */", pascal).unwrap();
    writeln!(out, "export interface {}Document extends {}Data {{", pascal, pascal).unwrap();
    writeln!(out, "  id: string;").unwrap();
    writeln!(out, "  created_at?: string;").unwrap();
    writeln!(out, "  updated_at?: string;").unwrap();
    writeln!(out, "}}\n").unwrap();
}

fn render_collection_slug_type(out: &mut String, registry: &Registry) {
    let slugs = sorted_collection_slugs(registry);
    if slugs.is_empty() {
        return;
    }
    let union = slugs.iter().map(|s| format!("\"{}\"", s)).collect::<Vec<_>>().join(" | ");
    writeln!(out, "/** All collection slugs */").unwrap();
    writeln!(out, "export type CollectionSlug = {};", union).unwrap();
}

fn write_field(out: &mut String, field: &FieldDefinition) {
    let ts_type = field_to_ts(field, "");
    let opt = if is_optional(field) { "?" } else { "" };
    writeln!(out, "  {}{}: {};", field.name, opt, ts_type).unwrap();
}

fn write_field_with_context(out: &mut String, field: &FieldDefinition, parent_pascal: &str) {
    let ts_type = field_to_ts(field, parent_pascal);
    let opt = if is_optional(field) { "?" } else { "" };
    writeln!(out, "  {}{}: {};", field.name, opt, ts_type).unwrap();
}

fn field_to_ts(field: &FieldDefinition, parent_pascal: &str) -> String {
    match &field.field_type {
        FieldType::Text | FieldType::Textarea | FieldType::Email | FieldType::Date
        | FieldType::Richtext => "string".to_string(),
        FieldType::Number => "number".to_string(),
        FieldType::Checkbox => "boolean".to_string(),
        FieldType::Json => "unknown".to_string(),
        FieldType::Select => {
            if field.options.is_empty() {
                "string".to_string()
            } else {
                field.options.iter()
                    .map(|o| format!("\"{}\"", o.value))
                    .collect::<Vec<_>>()
                    .join(" | ")
            }
        }
        FieldType::Relationship => {
            match &field.relationship {
                Some(rc) if rc.has_many => "string[]".to_string(),
                _ => "string".to_string(),
            }
        }
        FieldType::Array => {
            if field.fields.is_empty() {
                "Record<string, unknown>[]".to_string()
            } else {
                format!("{}{}[]", parent_pascal, to_pascal_case(&field.name))
            }
        }
        FieldType::Group => "Record<string, unknown>".to_string(),
        FieldType::Upload => "string".to_string(),
        FieldType::Blocks => "Record<string, unknown>[]".to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::core::collection::{
        CollectionAccess, CollectionAdmin, CollectionHooks, CollectionLabels,
    };
    use crate::core::field::{BlockDefinition, LocalizedString, RelationshipConfig, SelectOption};

    fn text_field(name: &str, required: bool) -> FieldDefinition {
        FieldDefinition {
            name: name.to_string(),
            required,
            ..Default::default()
        }
    }

    fn select_field(name: &str, opts: &[&str]) -> FieldDefinition {
        FieldDefinition {
            name: name.to_string(),
            field_type: FieldType::Select,
            required: true,
            options: opts.iter().map(|v| SelectOption {
                label: LocalizedString::Plain(v.to_string()), value: v.to_string(),
            }).collect(),
            ..Default::default()
        }
    }

    fn make_col(slug: &str, fields: Vec<FieldDefinition>) -> CollectionDefinition {
        CollectionDefinition {
            slug: slug.to_string(),
            labels: CollectionLabels::default(),
            timestamps: true,
            fields,
            admin: CollectionAdmin::default(),
            hooks: CollectionHooks::default(),
            auth: None,
            upload: None,
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        }
    }

    #[test]
    fn typescript_collection_output() {
        let col = make_col("posts", vec![
            text_field("title", true),
            text_field("content", false),
            select_field("status", &["draft", "published"]),
        ]);

        let mut out = String::new();
        render_collection(&mut out, &col);

        assert!(out.contains("export interface PostsData {"));
        assert!(out.contains("  title: string;"));
        assert!(out.contains("  content?: string;"));
        assert!(out.contains("  status: \"draft\" | \"published\";"));
        assert!(out.contains("export interface PostsDocument extends PostsData {"));
        assert!(out.contains("  id: string;"));
        assert!(out.contains("  created_at?: string;"));
        assert!(out.contains("  updated_at?: string;"));
    }

    #[test]
    fn typescript_global_output() {
        let global = GlobalDefinition {
            slug: "site_settings".to_string(), labels: CollectionLabels::default(),
            fields: vec![text_field("site_name", true)],
            hooks: CollectionHooks::default(), access: CollectionAccess::default(),
            live: None,
            versions: None,
        };

        let mut out = String::new();
        render_global(&mut out, &global);

        assert!(out.contains("export interface SiteSettingsData {"));
        assert!(out.contains("export interface SiteSettingsDocument extends SiteSettingsData {"));
    }

    #[test]
    fn typescript_relationship_has_many() {
        let col = make_col("posts", vec![
            FieldDefinition {
                name: "tags".to_string(),
                field_type: FieldType::Relationship,
                relationship: Some(RelationshipConfig {
                    collection: "tags".to_string(),
                    has_many: true,
                    max_depth: None,
                }),
                ..Default::default()
            },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("tags?: string[];"));
    }

    #[test]
    fn typescript_relationship_has_one() {
        let col = make_col("posts", vec![
            FieldDefinition {
                name: "author".to_string(),
                field_type: FieldType::Relationship,
                required: true,
                relationship: Some(RelationshipConfig {
                    collection: "users".to_string(),
                    has_many: false,
                    max_depth: None,
                }),
                ..Default::default()
            },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("  author: string;"));
    }

    #[test]
    fn typescript_number_checkbox_json_fields() {
        let col = make_col("items", vec![
            FieldDefinition { name: "price".to_string(), field_type: FieldType::Number, required: true, ..Default::default() },
            FieldDefinition { name: "active".to_string(), field_type: FieldType::Checkbox, ..Default::default() },
            FieldDefinition { name: "meta".to_string(), field_type: FieldType::Json, ..Default::default() },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("  price: number;"));
        assert!(out.contains("  active?: boolean;"));
        assert!(out.contains("  meta?: unknown;"));
    }

    #[test]
    fn typescript_array_with_subfields() {
        let col = make_col("posts", vec![
            FieldDefinition {
                name: "items".to_string(),
                field_type: FieldType::Array,
                fields: vec![text_field("label", true), text_field("value", false)],
                ..Default::default()
            },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("export interface PostsItems {"));
        assert!(out.contains("  label: string;"));
        assert!(out.contains("  value?: string;"));
        assert!(out.contains("PostsItems[]"));
    }

    #[test]
    fn typescript_array_without_subfields() {
        let col = make_col("posts", vec![
            FieldDefinition {
                name: "data".to_string(),
                field_type: FieldType::Array,
                ..Default::default()
            },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("Record<string, unknown>[]"));
    }

    #[test]
    fn typescript_group_field() {
        let col = make_col("items", vec![
            FieldDefinition { name: "seo".to_string(), field_type: FieldType::Group, ..Default::default() },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("Record<string, unknown>"));
    }

    #[test]
    fn typescript_blocks_field() {
        let col = make_col("pages", vec![
            FieldDefinition {
                name: "content".to_string(),
                field_type: FieldType::Blocks,
                blocks: vec![BlockDefinition {
                    block_type: "text".to_string(),
                    fields: vec![text_field("body", true)],
                    label: Some(LocalizedString::Plain("Text".to_string())),
                    ..Default::default()
                }],
                ..Default::default()
            },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("Record<string, unknown>[]"));
    }

    #[test]
    fn typescript_upload_field() {
        let col = make_col("items", vec![
            FieldDefinition { name: "image".to_string(), field_type: FieldType::Upload, required: true, ..Default::default() },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("  image: string;"));
    }

    #[test]
    fn typescript_select_without_options() {
        let col = make_col("items", vec![
            FieldDefinition { name: "category".to_string(), field_type: FieldType::Select, required: true, ..Default::default() },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("  category: string;"));
    }

    #[test]
    fn typescript_collection_slug_type() {
        let mut registry = Registry::new();
        registry.register_collection(make_col("posts", vec![text_field("title", true)]));
        registry.register_collection(make_col("pages", vec![text_field("body", true)]));
        let out = render(&registry);
        assert!(out.contains("export type CollectionSlug = \"pages\" | \"posts\";"));
    }

    #[test]
    fn typescript_collection_slug_type_empty() {
        let registry = Registry::new();
        let out = render(&registry);
        assert!(!out.contains("CollectionSlug"));
    }

    #[test]
    fn typescript_no_timestamps() {
        let mut col = make_col("tags", vec![text_field("name", true)]);
        col.timestamps = false;
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(!out.contains("created_at"));
        assert!(!out.contains("updated_at"));
    }

    #[test]
    fn typescript_email_date_richtext_textarea() {
        let col = make_col("items", vec![
            FieldDefinition { name: "contact".to_string(), field_type: FieldType::Email, required: true, ..Default::default() },
            FieldDefinition { name: "at".to_string(), field_type: FieldType::Date, required: true, ..Default::default() },
            FieldDefinition { name: "body".to_string(), field_type: FieldType::Richtext, required: true, ..Default::default() },
            FieldDefinition { name: "notes".to_string(), field_type: FieldType::Textarea, required: true, ..Default::default() },
        ]);
        let mut out = String::new();
        render_collection(&mut out, &col);
        assert!(out.contains("  contact: string;"));
        assert!(out.contains("  at: string;"));
        assert!(out.contains("  body: string;"));
        assert!(out.contains("  notes: string;"));
    }

    #[test]
    fn typescript_full_render_with_globals() {
        let mut registry = Registry::new();
        registry.register_collection(make_col("posts", vec![text_field("title", true)]));
        registry.register_global(GlobalDefinition {
            slug: "settings".to_string(),
            labels: CollectionLabels::default(),
            fields: vec![text_field("name", true)],
            hooks: CollectionHooks::default(),
            access: CollectionAccess::default(),
            live: None,
            versions: None,
        });
        let out = render(&registry);
        assert!(out.contains("Auto-generated"));
        assert!(out.contains("export interface PostsData {"));
        assert!(out.contains("export interface SettingsData {"));
        assert!(out.contains("export interface SettingsDocument extends SettingsData {"));
        assert!(out.contains("export type CollectionSlug = \"posts\";"));
    }
}
